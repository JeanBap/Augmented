#!/usr/bin/env node
/**
 * Post-deploy search engine notification script for raisereadybook.com
 *
 * Pings:
 * 1. Google - sitemap ping (deprecated but still sometimes honored)
 * 2. Bing/Yandex/Others via IndexNow - instant indexing
 * 3. Google Indexing API - if credentials available (for JobPosting pages)
 *
 * Run after deploy: node scripts/ping-search-engines.js
 */

const https = require('https');
const fs = require('fs');
const path = require('path');

const SITE_URL = 'https://www.raisereadybook.com';
const SITEMAP_URL = `${SITE_URL}/sitemap.xml`;
const INDEXNOW_KEY = '4077e1cae31fe9d1f9b12e4768fad13e';

function httpGet(url) {
  return new Promise((resolve, reject) => {
    https.get(url, (res) => {
      let data = '';
      res.on('data', chunk => data += chunk);
      res.on('end', () => resolve({ status: res.statusCode, data }));
    }).on('error', reject);
  });
}

function httpPost(url, body, headers = {}) {
  return new Promise((resolve, reject) => {
    const urlObj = new URL(url);
    const options = {
      hostname: urlObj.hostname,
      path: urlObj.pathname,
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
        'Content-Length': Buffer.byteLength(body),
        ...headers
      }
    };
    const req = https.request(options, (res) => {
      let data = '';
      res.on('data', chunk => data += chunk);
      res.on('end', () => resolve({ status: res.statusCode, data }));
    });
    req.on('error', reject);
    req.write(body);
    req.end();
  });
}

async function pingGoogle() {
  console.log('\n[Google] Pinging sitemap...');
  try {
    const res = await httpGet(`https://www.google.com/ping?sitemap=${encodeURIComponent(SITEMAP_URL)}`);
    console.log(`  Status: ${res.status} ${res.status === 200 ? 'OK' : 'WARN'}`);
  } catch (e) {
    console.log(`  Error: ${e.message}`);
  }
}

async function pingBingSitemap() {
  console.log('\n[Bing] Pinging sitemap...');
  try {
    const res = await httpGet(`https://www.bing.com/ping?sitemap=${encodeURIComponent(SITEMAP_URL)}`);
    console.log(`  Status: ${res.status} ${res.status === 200 ? 'OK' : 'WARN'}`);
  } catch (e) {
    console.log(`  Error: ${e.message}`);
  }
}

async function submitIndexNow() {
  console.log('\n[IndexNow] Submitting URLs to Bing, Yandex, Seznam, Naver...');

  // Read URL list generated by sitemap script
  const urlListPath = path.join(__dirname, 'url-list.txt');
  if (!fs.existsSync(urlListPath)) {
    console.log('  No url-list.txt found. Run generate-sitemap.js first.');
    return;
  }

  const urls = fs.readFileSync(urlListPath, 'utf8')
    .split('\n')
    .filter(u => u.trim());

  // IndexNow supports batch submission of up to 10,000 URLs
  // Submit to multiple engines via api.indexnow.org (broadcasts to all participants)
  const payload = JSON.stringify({
    host: 'www.raisereadybook.com',
    key: INDEXNOW_KEY,
    keyLocation: `${SITE_URL}/${INDEXNOW_KEY}.txt`,
    urlList: urls.slice(0, 10000) // IndexNow limit
  });

  try {
    const res = await httpPost('https://api.indexnow.org/indexnow', payload);
    console.log(`  Submitted ${urls.length} URLs`);
    console.log(`  Status: ${res.status} ${res.status === 200 || res.status === 202 ? 'OK' : 'WARN'}`);
    if (res.status !== 200 && res.status !== 202) {
      console.log(`  Response: ${res.data.substring(0, 200)}`);
    }
  } catch (e) {
    console.log(`  Error: ${e.message}`);
  }
}

async function main() {
  console.log('=== Search Engine Ping Script ===');
  console.log(`Site: ${SITE_URL}`);
  console.log(`Time: ${new Date().toISOString()}`);

  await Promise.all([
    pingGoogle(),
    pingBingSitemap(),
    submitIndexNow(),
  ]);

  console.log('\n=== Done ===');
}

main().catch(console.error);
